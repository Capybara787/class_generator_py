class pet:
	def __init__(self, name, age, type): 
		self.name = name
		self.age = age
		self.type = type
	def get_name(self): 
		return self.name
	def set_name(self, name):
		self.name = name
	def get_age(self): 
		return self.age
	def set_age(self, age):
		self.age = age
	def get_type(self): 
		return self.type
	def set_type(self, type):
		self.type = type
